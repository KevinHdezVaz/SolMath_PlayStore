package com.solumath.calculator.adaptadores;

public class lista {

    private int imgFoto;
    private String titulo;

    public lista(int imgFoto, String titulo) {
        this.imgFoto = imgFoto;
        this.titulo = titulo;
    }

    public int getImgFoto() {
        return imgFoto;
    }

    public String getTitulo() {
        return titulo;
    }
}
